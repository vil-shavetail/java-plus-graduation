package ru.practicum.enumeration;

/**
 * Действия для изменения состояния события
 */
public enum StateAction {
    /**
     * Опубликовать событие (доступно администратору)
     */
    PUBLISH_EVENT,

    /**
     * Отклонить событие (доступно администратору)
     */
    REJECT_EVENT,

    /**
     * Отправить событие на модерацию (доступно инициатору)
     */
    SEND_TO_REVIEW,

    /**
     * Отменить событие/отменить модерацию (доступно инициатору)
     */
    CANCEL_REVIEW
}